    private void configurarTabla() {
        String[] columnNames = {"ID", "Producto", "Cantidad", "Precio Unitario", "Subtotal"};
        model = new DefaultTableModel(columnNames, 0) {
            @Override
            public boolean isCellEditable(int row, int column) {
                return false;
            }
        };
        tblDetalles.setModel(model);
    }

    private void cargarDetalles() {
        try {
            model.setRowCount(0);
            List<DetalleCompra> detalles = detalleDAO.obtenerTodos(); // <<<< CAMBIO AQUÍ
            for (DetalleCompra d : detalles) {
                model.addRow(new Object[]{
                    d.getIdDetalle(),
                    obtenerNombreProducto(d.getIdProducto()),
                    d.getCantidad(),
                    d.getPrecioUnitario(),
                    d.getSubtotal()
                });
            }
        } catch (SQLException ex) {
            JOptionPane.showMessageDialog(this,
                "Error al cargar detalles: " + ex.getMessage(),
                "Error", JOptionPane.ERROR_MESSAGE);
        }
    }

    private String obtenerNombreProducto(long idProducto) {
        try (Connection conn = DatabaseConnection.getConnection();
             PreparedStatement stmt = conn.prepareStatement(
                 "SELECT producto FROM productos WHERE idproducto = ?")) {
            stmt.setLong(1, idProducto);
            ResultSet rs = stmt.executeQuery();
            return rs.next() ? rs.getString("producto") : "Desconocido";
        } catch (SQLException ex) {
            ex.printStackTrace();
            return "Error";
        }
    }

