    private void btnEliminarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEliminarActionPerformed
        int filaSeleccionada = tblDetalles.getSelectedRow();
    if (filaSeleccionada >= 0) {
        int idDetalle = (int) model.getValueAt(filaSeleccionada, 0); // ID está en la primera columna
        int confirmacion = JOptionPane.showConfirmDialog(
            this, 
            "¿Está seguro de eliminar este detalle?", 
            "Confirmar eliminación", 
            JOptionPane.YES_NO_OPTION
        );

        if (confirmacion == JOptionPane.YES_OPTION) {
            try {
                detalleDAO.eliminar(idDetalle);
                cargarDetalles();
                JOptionPane.showMessageDialog(this, 
                    "Detalle eliminado correctamente", 
                    "Éxito", JOptionPane.INFORMATION_MESSAGE);
            } catch (SQLException ex) {
                JOptionPane.showMessageDialog(this, 
                    "Error al eliminar detalle: " + ex.getMessage(), 
                    "Error", JOptionPane.ERROR_MESSAGE);
            }
        }
    } else {
        JOptionPane.showMessageDialog(this, 
            "Seleccione un detalle de la tabla", 
            "Advertencia", JOptionPane.WARNING_MESSAGE);
    }
    }//GEN-LAST:event_btnEliminarActionPerformed

