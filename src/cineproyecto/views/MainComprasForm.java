    private void configurarTabla() {
        String[] columnNames = {"ID", "Cliente", "Empleado", "Fecha"};
        
        model = new DefaultTableModel(columnNames, 0) {
            @Override
            public boolean isCellEditable(int row, int column) {
                return false; // Tabla no editable
            }
        };
        
        tblcompras.setModel(model);
    }
    
    private void cargarCompras() {
        try {
            model.setRowCount(0); // Limpiar tabla
            List<Compra> compras = compraDAO.obtenerTodos();
            
            for (Compra c : compras) {
                model.addRow(new Object[]{
                    c.getIdCompra(),
                    obtenerNombreCliente(c.getIdCliente()),
                    obtenerNombreEmpleado(c.getIdEmpleado()),
                    c.getFechaCompra()
                });
            }
        } catch (SQLException ex) {
            JOptionPane.showMessageDialog(this, 
                "Error al cargar compras: " + ex.getMessage(), 
                "Error", JOptionPane.ERROR_MESSAGE);
        }
    }
    
    private String obtenerNombreCliente(String idCliente) {
        try (Connection conn = DatabaseConnection.getConnection();
             PreparedStatement stmt = conn.prepareStatement(
                 "SELECT nombre_cliente FROM clientes WHERE idcliente = ?")) {
            
            stmt.setString(1, idCliente);
            ResultSet rs = stmt.executeQuery();
            
            return rs.next() ? rs.getString("nombre_cliente") : "Desconocido";
            
        } catch (SQLException ex) {
            ex.printStackTrace();
            return "Error";
        }
    }
    
    private String obtenerNombreEmpleado(String idEmpleado) {
        try (Connection conn = DatabaseConnection.getConnection();
             PreparedStatement stmt = conn.prepareStatement(
                 "SELECT nombre_empleado FROM empleados WHERE id_empleado = ?")) {
            
            stmt.setString(1, idEmpleado);
            ResultSet rs = stmt.executeQuery();
            
            return rs.next() ? rs.getString("nombre_empleado") : "Desconocido";
            
        } catch (SQLException ex) {
            ex.printStackTrace();
            return "Error";
        }
    }

