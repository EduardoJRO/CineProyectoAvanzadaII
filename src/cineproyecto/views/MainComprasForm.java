    private void btnEditarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEditarActionPerformed
        int filaSeleccionada = tblcompras.getSelectedRow();
        if (filaSeleccionada >= 0) {
            int idCompra = (int) model.getValueAt(filaSeleccionada, 0);
            try {
                Compra compra = compraDAO.obtenerPorId(idCompra);
                ComprasDialog dialog = new ComprasDialog(this, true, compra);
                dialog.setLocationRelativeTo(this);
                dialog.setVisible(true);
                cargarCompras();
            } catch (SQLException ex) {
                JOptionPane.showMessageDialog(this, 
                    "Error al cargar compra: " + ex.getMessage(), 
                    "Error", JOptionPane.ERROR_MESSAGE);
            }
        } else {
            JOptionPane.showMessageDialog(this, 
                "Seleccione una compra de la tabla", 
                "Advertencia", JOptionPane.WARNING_MESSAGE);
        }
    }//GEN-LAST:event_btnEditarActionPerformed

    private void btnEliminarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEliminarActionPerformed
        int filaSeleccionada = tblcompras.getSelectedRow();
        if (filaSeleccionada >= 0) {
            int idCompra = (int) model.getValueAt(filaSeleccionada, 0);
            int confirmacion = JOptionPane.showConfirmDialog(
                this, 
                "¬øEst√° seguro de eliminar esta compra?", 
                "Confirmar eliminaci√≥n", 
                JOptionPane.YES_NO_OPTION
            );
            
            if (confirmacion == JOptionPane.YES_OPTION) {
                try {
                    compraDAO.eliminar(idCompra);
                    cargarCompras();
                    JOptionPane.showMessageDialog(this, 
                        "Compra eliminada correctamente", 
                        "√âxito", JOptionPane.INFORMATION_MESSAGE);
                } catch (SQLException ex) {
                    JOptionPane.showMessageDialog(this, 
                        "Error al eliminar compra: " + ex.getMessage(), 
                        "Error", JOptionPane.ERROR_MESSAGE);
                }
            }
        } else {
            JOptionPane.showMessageDialog(this, 
                "Seleccione una compra de la tabla", 
                "Advertencia", JOptionPane.WARNING_MESSAGE);
        }
    }//GEN-LAST:event_btnEliminarActionPerformed

